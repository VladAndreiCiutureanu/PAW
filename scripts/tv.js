const sidebarItems = document.querySelectorAll('.sidebar-menu-item');
const mediaGrid = document.querySelector('.media-grid');
const pageTitle = document.querySelector('.content-header h2');
const sortDropdown = document.querySelector('.sort-dropdown');

let currentCategory = 'trending';

sidebarItems.forEach(value => {
    value.addEventListener('click', () => {
        sidebarItems.forEach(item => item.classList.remove('active'));
        value.classList.add('active');
        
        currentCategory = value.getAttribute('data-category');
        const titleText = value.innerText;

        pageTitle.innerText = `${titleText} TV Shows`;

        sortAndLoadTVShows();
    });
});

sortDropdown.addEventListener('change', () => {
    sortAndLoadTVShows();
});

function sortAndLoadTVShows() {
    let showsToDisplay = [...tvDatabase[currentCategory]];
    const sortMethod = sortDropdown.value;

    if (sortMethod === 'newest') {
        showsToDisplay.sort((a, b) => {
            const yearA = parseInt(a.meta) || 0;
            const yearB = parseInt(b.meta) || 0;
            return yearB - yearA;
        });
    } else if (sortMethod === 'rating') {
        showsToDisplay.sort((a, b) => b.rating - a.rating);
    } else if (sortMethod === 'popular') {
        showsToDisplay.sort((a, b) => b.popularity - a.popularity);
    }

    renderCards(showsToDisplay);
}

function renderCards(showsArray) {
    mediaGrid.innerHTML = '';
    
    if(!showsArray || showsArray.length === 0){
        mediaGrid.innerHTML = '<p style="color: rgba(255,255,255,0.5);">No TV shows found!</p>';
        return;
    }
    
    showsArray.forEach(show => {
        const cardHTML = `
            <div class="media-card">
                <div class="card-image-wrapper">
                    <img src="${show.image}" alt="${show.title} Poster" class="card-image">
                    <div class="card-overlay">
                        <button class="add-to-vault-button">&plus; Vault</button>
                    </div>
                </div>
                <div class="card-info">
                    <h3>${show.title}</h3>
                    <div style="display: flex; justify-content: space-between; align-items: center;">
                        <p class="card-meta">${show.meta}</p>
                        <p style="color: #FDBB2D; font-weight: bold; margin: 0; font-size: 12px;">
                            &#9733; ${show.rating || 'N/A'}
                        </p>
                    </div>
                </div>
            </div>
        `;
        mediaGrid.innerHTML += cardHTML;
    });
}

window.onload = () => { sortAndLoadTVShows(); };